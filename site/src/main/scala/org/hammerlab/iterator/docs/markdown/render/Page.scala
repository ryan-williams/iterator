package org.hammerlab.iterator.docs.markdown.render

import org.hammerlab.iterator.docs.markdown.Elem
import org.hammerlab.iterator.docs.markdown.render.Page.Loc

case class Page(loc: Loc,
                content: Section)

object Page {
  type Loc = String
  def apply(loc: Loc, content: Elem.Section): Page =
    Page(
      loc,
      Section(
        Id(Nil),
        content
      )
    )
}
